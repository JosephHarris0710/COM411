direction = str(input("Towards which direction should i paint (up, down, left and right)? "))

if (direction == "up"):
  print("I am painting in the upwards direction!")
elif (direction == "down"):
  print("I am painting in the downwards direction!")
elif (direction == "left"):
  print("I am painting in the leftward direction!")
elif (direction == "right"):
  print("I am painting in the rightward direction")
else:
  print("That is not a valid direction!")